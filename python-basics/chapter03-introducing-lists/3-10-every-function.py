mountains = ['bridgers', 'tabacco roots', 'spanish peaks', 'gallatin range', 'madison range']

print(mountains)

## Get Length of List
print(len(mountains))

## Sort Alphabetically
print(sorted(mountains))

## Sort Alphabetically, Z-A
print(sorted(mountains, reverse=True))

## Reverse List
mountains.reverse()
print(mountains)

## Permanently Sort Alphabetically
print(mountains.sort())